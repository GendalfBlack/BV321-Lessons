# Задача 2. Користувач пише текст, який програма має записати у файл, але у файл не має потрапити
# жодна цифра.
text = input("Введіть текст: ")

f = open("text.txt", "w")

for s in text:
    if not s.isdigit():
        f.write(s)

f.close()

